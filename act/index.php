            <h3 class="section-title" id="envaliditas">1. Validity Range</h3>
            <p>
              This Valorant Fun Match tournament is organized by the Act Development Lab committee. This document is the only Rules Book that applies to the Valorant Fun Match for both Teams and Players, as well as all matches played in the Valorant Fun Match tournament. By participating in this Valorant Fun Match, participants declare that they understand and accept all the rules set forth in this Rules Book.
            </p>
          
            <h3 class="section-title" id="enpeserta">2. Participant</h3>
            <p>
              Participants are teams or players who participate in this fun match. They register through the form provided by the committee. Players are not allowed to join more than one team during the Valorant Fun Match.
            </p>
            <ul>
              <li>
                <b class="list-title">Number of Players</b>
                <br>
                <ul>
                  <li>Each team must have a maximum of six players consisting of <b>five</b> core players and <b>one</b> substitute player.</li>
                </ul>
              </li>
              <li>
                <b class="list-title">Prohibition of Team/Player</b>
                <br>
                <ul type="o">
                  <li>All participants are <b>prohibited</b> from intentionally or unintentionally exploiting <b>game bugs</b>.</li>
                  <li>All participants are <b>prohibited</b> from intentionally or unintentionally using any <b>third-party apps</b> that affect the course of the match.</li>
                  <li>Participants or teams that receive warnings/bans from the developer/Valorant Riot Games are not allowed to participate in this fun match.</li>
                  <li>Nicknames that are not listed on the registration form are prohibited from participating in the game and entering the match lobby.</li>
                  <li>If any participant or team is found to have committed any violations, the committee <b>has the right</b> to impose <b>penalties</b> and <b>announce</b> the violated rules along with the related rule violator.</li>
                </ul>
              </li>
              <li>
                <b class="list-title">Committee</b>
                <br>
                <ul>
                  <li>The committee is the Organizing Committee formed to run the Valorant Fun Match event to ensure that the event runs smoothly.</li>
                  <li>The committee will conduct a light selection from the registration form to ensure the smooth running of the event.</li>
                </ul>
              </li>
              <li>
                <b class="list-title">Time Zone</b>
                <br>
                <ul>
                  <li>All time zones used in this tournament (19:00 - 00:00) are in Western Indonesian Time (WIB) - UTC +7 / GMT +7.</li>
                </ul>
              </li>
            </ul>

