            <h3 class="section-title" id="kehadiran">8. Mulai Pertandingan</h3>
            <ul>
              <li>
                <b class="list-title">Ketepatan Waktu</b>
                <br>
                Seluruh tim wajib hadir 20 menit sebelum pertandingan dimulai. Semua tim dan pemain harus berada di lobi game selambat-lambatnya 10 menit sebelum pertandingan dimulai.<br><br>
              </li>
              <li>
                <b class="list-title">Waktu Keterlambatan</b>
                <br>
                Batas waktu keterlambatan hanya 10 menit dari jadwal yang sudah ditentukan. Apabila tim belum lengkap atau belum hadir lebih dari waktu keterlambatan yang sudah ditentukan maka tim tersebut tidak bisa mengikuti pertandingan/diskualifikasi.<br><br>
              </li>
              <li>
                <b class="list-title">Minimal Pemain</b>
                <br>
                Apabila ada gangguan pada suatu tim dan tiga pemain tidak bisa ikut dalam pertandingan, maka tim tersebut dianggap kalah/gugur. <br><br>
              </li>
              <li>
                <b class="list-title">Tidak Hadir</b>
                <br>
                Jika tim/pemain tidak dapat hadir dengan alasan apapun sesuai dengan waktu pertandingan resmi dimulai, tim akan dianggap gugur dari turnamen.<br><br>
              </li>
            </ul>

