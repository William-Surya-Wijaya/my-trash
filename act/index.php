            <h3 class="section-title" id="validitas">1. Rentang Validitas</h3>
            <p>
              Pertandingan Valorant Fun Match dilaksanakan oleh panitia Act Development Lab. Dokumen ini adalah satu-satunya Rules Book yang berlaku untuk Valorant Fun Match bagi para Tim dan Pemain serta semua pertandingan yang dimainkan dalam turnamen Valorant Fun Match. Dengan berpartisipasi dalam Valorant Fun Match ini peserta menyatakan bahwa mereka memahami dan menerima semua aturan yang ditentukan dalam Rules Book ini.
            </p>
          
            <h3 class="section-title" id="peserta">2. Peserta</h3>
            <p>
              Peserta adalah tim atau pemain yang berpartisipasi dalam fun match ini. Dan mendaftar melewati form yang sudah disediakan oleh panitia. Pemain tidak diperbolehkan bergabung lebih dari satu tim selama Valorant Fun Match berlangsung
            </p>
            <ul>
              <li>
                <b class="list-title">Jumlah Pemain</b>
                <br>
                <ul>
                  <li>Maksimal setiap tim harus berjumlah enam pemain yang terdiri dari <b>lima</b> pemain inti dan <b>satu</b> pemain cadangan.</li>
                </ul>
              </li>
              <li>
                <b class="list-title">Pelarangan Tim/Pemain</b>
                <br>
                <ul type="o">
                  <li>Semua peserta <b>dilarang</b> dengan sengaja atau tanpa sengaja mengeksploitasi <b>bug game</b>.</li>
                  <li>Semua peserta <b>dilarang</b> dengan sengaja atau tanpa sengaja menggunakan semua <b>third-party app</b> yang mempengaruhi jalannya pertadingan.</li>
                  <li>Peserta atau tim yang mendapatkan peringatan/ban dari developer/Valorant Riot Games dilarang mengikuti fun match ini.</li>
                  <li>Nickname diluar list nickname yang tertera pada form pendaftaran dilarang untuk ikut bermain dan masuk ke lobby pertandingan.</li>
                  <li>Bila ada peserta atau tim yang terbukti melakukan pelanggaran apapun, maka panitia <b>berhak</b> memberikan <b>penalti</b> dan <b>mengumumkan</b> peraturan yang dilanggar berserta pelanggar peraturan terkait.</li>
                </ul>
              </li>
              <li>
                <b class="list-title">Panitia</b>
                <br>
                <ul>
                  <li>Panitia adalah Organizing Committee yang dibentuk untuk menjalankan Valorant Fun Match agar event berjalan dengan baik.</li>
                  <li>Panitia akan melakukan seleksi ringan dari form pendaftaran untuk menjada jalannya event.</li>
                </ul>
              </li>
              <li>
                <b class="list-title">Zona Waktu</b>
                <br>
                <ul>
                  <li>Semua zona waktu yang digunakan dalam turnamen ini (Pukul 19:00 - 00:00) menggunakan Waktu Indonesia Barat (WIB).</li>
                </ul>
              </li>
            </ul>

